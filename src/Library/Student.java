        public void Load_student()
        {
        try {
            int c;
            prepare=con.prepareStatement("select * from student");
            result=prepare.executeQuery();
            ResultSetMetaData res=result.getMetaData();
            c=res.getColumnCount();
            DefaultTableModel d=(DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            while(result.next())
            {
                Vector v=new Vector();
                for(int i=1;i<=c;i++)
                {
                    v.add(result.getString("S_id"));
                    v.add(result.getString("S_Name"));
                    v.add(result.getString("Email"));
                    v.add(result.getString("Address"));
                    v.add(result.getString("phone_Number"));
                    
                }
                d.addRow(v);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        }
       
            
            
       

