    private void SidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SidKeyPressed
      
        if(evt.getKeyCode()==KeyEvent.VK_ENTER)
        {
        try {
            String id=Sid.getText();
            prepare=con.prepareStatement("select s.S_Name,p.P_Name,i.Return_Date,DATEDIFF(now(),i.Return_Date) as late_days from issue_book i INNER JOIN student s on s.S_id=i.S_id INNER JOIN publication p on i.B_id=p.B_id and i.S_id=?");
            prepare.setString(1, id);
            result=prepare.executeQuery();
            if(result.next()==false)
            {
                JOptionPane.showMessageDialog(this, "Student ID not found");
            }
            else
            {
                String SName=result.getString("s.S_Name");
                String PName=result.getString("p.P_Name");
                S1.setText(SName);
                B1.setText(PName);
                String Date=result.getString("i.Return_Date");
                D2.setText(Date);
                String elp=result.getString("late_days");
                int n_elp=Integer.parseInt(elp);
                if(n_elp>0)
                {
                     LD.setText(elp);
                    int fine=n_elp*10;
                    Fine.setText(String.valueOf(fine));
                }
                else
                {
                    LD.setText("0");
                    Fine.setText("0");
                }
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(ReturnPublication.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_SidKeyPressed

