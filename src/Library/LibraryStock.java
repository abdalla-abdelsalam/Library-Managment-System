        public void Load_Publication()
        {
        try {
            int c;
            prepare=con.prepareStatement("select * from publication");
            result=prepare.executeQuery();
            ResultSetMetaData res=result.getMetaData();
            c=res.getColumnCount();
            DefaultTableModel d=(DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while(result.next())
            {
                Vector v=new Vector();
                for(int i=1;i<=c;i++)
                {
                    v.add(result.getString("B_id"));
                    v.add(result.getString("P_Name"));
                    v.add(result.getString("Publication_Type"));
                    v.add(result.getString("Edition"));
                    v.add(result.getString("Author"));
                    v.add(result.getString("publisher"));
                    v.add(result.getString("N_Pages"));
                }
                d.addRow(v);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(LibraryStock.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        }
       
            
            
       

