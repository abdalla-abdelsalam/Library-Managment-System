        public void Load_Publication()
        {
        try {
            prepare=con.prepareStatement("select * from publication");
              result=prepare.executeQuery();
              Publication.removeAllItems();
                while(result.next())
                {
                    Publication.addItem(new publicationItem(result.getInt("B_id"),result.getString("P_Name")));
                }
              
        } catch (SQLException ex) {
            Logger.getLogger(IssePublication.class.getName()).log(Level.SEVERE, null, ex);
        }
          
           
            
            
        }
       public void Load_Issue_Publication()
        {
            int c;
        try {
            prepare=con.prepareStatement("select i.I_id,s.S_Name,p.P_Name,i.Issue_Date,i.Return_Date from issue_book i INNER JOIN student s on s.S_id=i.S_id INNER JOIN publication p on i.B_id=p.B_id");
              result=prepare.executeQuery();
         
             ResultSetMetaData res=result.getMetaData();
            c=res.getColumnCount();
            DefaultTableModel d=(DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            while(result.next())
            {
                Vector v=new Vector();
                for(int i=1;i<=c;i++)
                {
                    v.add(result.getString("i.I_id"));
                    v.add(result.getString("s.S_Name"));
                    v.add(result.getString("p.P_Name"));
                    v.add(result.getString("i.Issue_Date"));
                    v.add(result.getString("i.Return_Date"));
                    
                }
                d.addRow(v);
                
            }
              
        } catch (SQLException ex) {
            Logger.getLogger(IssePublication.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
            
            
       

