    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String student_id=Sid.getText();
        publicationItem pItem=(publicationItem) Publication.getSelectedItem();
        SimpleDateFormat date_format1=new SimpleDateFormat("yyyy-MM-dd");
        String IsseDate=date_format1.format(Date.getDate());
        SimpleDateFormat date_format2=new SimpleDateFormat("yyyy-MM-dd");
        String ReturnBook=date_format2.format(RDate.getDate());
        
        
        try {
            prepare=con.prepareStatement("insert into issue_book(S_id,B_id,Issue_Date,Return_Date)values(?,?,?,?)");
            prepare.setString(1, student_id);
            prepare.setInt(2, pItem.id);
            prepare.setString(3,IsseDate);
            prepare.setString(4,ReturnBook);
            int k=prepare.executeUpdate();
            if(k==1)
            {
                JOptionPane.showMessageDialog(this, "publication Issued !!");
                
                Publication.setSelectedItem(-1);
                
                Sid.setText("");
                StudentName.setText("");
                Load_Issue_Publication();
                
            }
            else
            {
                JOptionPane.showMessageDialog(this, "error");
            }
        } catch (SQLException ex) {
            Logger.getLogger(IssePublication.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

